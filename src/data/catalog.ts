const A = (id: string, correct: string, others: string[]): Answer[] => {
  const all = [
    { id: `${id}-a1`, text: correct, isCorrect: true },
    ...others.map((t, i) => ({
      id: `${id}-a${i + 2}`,
      text: t,
      isCorrect: false,
    })),
  ];
  return all.sort(() => Math.random() - 0.5);
};

